// 接口配置项
var prefix = '/api/infra/smart/assistant/template/' ;
var managerUrl = {
    datatables : prefix +"datatables" ,
    createUrl: prefix + 'add' ,
    // saveUrl: prefix + 'save' ,
    saveUrl: prefix + 'saveOrUpdateTemplate' ,
    updateUrl: prefix +"modify" ,
    statusUrl: prefix +"changeStatus" ,
    cleanUrl: prefix + "clean",
    detailUrl: prefix +"detail",
    removeUrl: prefix + "delete" ,
    exportUrl: prefix + "exportExcel",
    changeField: prefix + "changeField",
    updateTemplateParamFormat: prefix + "updateTemplateParamFormat",
    catalogTreeSelect: prefix + "catalogTreeSelect",
    downloadfile: prefix + "downloadfile",
    getTemplateType: prefix + "getTemplateType",
}

// 获取模板类型
export function getTemplateType() {
    return request({
        url: managerUrl.getTemplateType ,
        method: 'get'
    })
}

// 更新模板参数
export function updateTemplateParamFormat(data) {
    return request({
        url: managerUrl.updateTemplateParamFormat ,
        method: 'post',
        data: data
    })
}

// 查询部门下拉树结构
export function catalogTreeSelect() {
  return request({
    url: managerUrl.catalogTreeSelect ,
    method: 'get'
  })
}

// 查询数据库列表
export function listTemplate(query) {
  return request({
    url: managerUrl.datatables ,
    method: 'post',
    params: query
  })
}

// 查询数据库详细
export function getTemplate(databaseId) {
  return request({
    url: managerUrl.detailUrl + '/' + parseStrEmpty(databaseId),
    method: 'get'
  })
}

// 新增数据库
export function addTemplate(data) {
  return request({
    url: managerUrl.saveUrl ,
    method: 'post',
    data: data
  })
}

// 修改数据库
export function updateTemplate(data) {
  return request({
    url: managerUrl.updateUrl ,
    method: 'put',
    data: data
  })
}

// 删除数据库
export function delTemplate(databaseId) {
  return request({
    url: managerUrl.removeUrl + '/' + parseStrEmpty(databaseId),
    method: 'delete'
  })
}

